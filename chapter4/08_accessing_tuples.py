# indexing - you can access individual elements of a tuple using an index, with the first element being at index 0. Indexing allows you to retrieve elements at specific positions.

# 1. Positive Indexing
tuple1 = (1,6,8,9 )
print(tuple1[3])
print(tuple1[0])

#2. Negative Indexing
tuple2 = (6,9,11,90,98)
print(tuple2[-3])
print(tuple2[-2])